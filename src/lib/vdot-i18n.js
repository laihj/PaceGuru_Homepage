export function isVdotLocale(locale) {
  return VDOT_LOCALES.includes(locale);
}

export function getVdotCopy(locale = DEFAULT_VDOT_LOCALE) {
  return copies[isVdotLocale(locale) ? locale : DEFAULT_VDOT_LOCALE];
}

export function vdotPath(locale = DEFAULT_VDOT_LOCALE) {
  return locale === DEFAULT_VDOT_LOCALE ? '/' : `/${locale}`;
}
